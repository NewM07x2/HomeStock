-- ======================================================
-- PostgreSQL Extensions for HomeStock
-- ======================================================
-- このスクリプトは必要な拡張機能を有効化します。
-- 実行順序: 最初に実行してください（他のスクリプトより前）
--
-- 必要な拡張機能:
--   - uuid-ossp: UUID生成関数を提供
--   - citext: 大文字小文字を区別しないテキスト型を提供
--
-- 注意:
--   - 拡張機能のインストールにはスーパーユーザー権限が必要な場合があります
--   - ホスティング環境によっては pgcrypto の gen_random_uuid() を推奨する場合があります
-- ======================================================

-- UUID生成関数を提供する拡張機能
-- uuid_generate_v4() 関数が使用可能になります
CREATE EXTENSION IF NOT EXISTS "uuid-ossp";

-- 大文字小文字を区別しないテキスト型を提供する拡張機能
-- メールアドレスなどの比較に便利です
CREATE EXTENSION IF NOT EXISTS "citext";
